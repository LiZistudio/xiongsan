int main()
{
	//向内存申请10个整型的空间
	//int* p = (int*)malloc(INT_MAX);//Not enough space
	int* p = (int*)malloc(10 * sizeof(int));
	if (p==NULL)
	{
		//打印错误原因的一个方式
		printf("%s\n", strerror(errno));
	}
	else
	{
		//正常使用空间
		int i = 0;
		for ( i = 0; i < 10; i++)
		{
			*(p + i) = i;
		}
		for (i = 0; i < 10; i++)
		{
			printf("%d ", *(p + i));
		}
	}
	//当动态申请的空间不再使用时应该还给操作系统
	free(p);//主动回收
	p = NULL;//free完之后的p仍然能访问原来的地址，但p已经被断开，所指向的内存空间被释放――野指针，所以将其置空是最安全的做法

	return 0;
}
